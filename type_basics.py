# Python Primitive Types
# Primitive types represent different kinds of values
# all of the values on the right of '=' operator represent
# different types of python primitives
example_int = 1  # type: integer
example_float = 2.5  # type: floating point
example_bool = True  # type: boolean
example_char = "a"  # type: character

print(example_int)
print(example_float)
print(example_bool)
print(example_char)

# Special built-in type
# While not technically a primitve
# strings are very common built-in value types
# used in python
example_string = "Hello World"  # type: string

print(example_string)

# Variables
# All of the text on the left of the '=' operator up
# to this point has been a variable
# Variables act as a way to store and "mutate" "state"
# Any time a value is needed to vary, it should be stored
# inside of a variable
example_bool1 = True  # Store the value True inside of the variable
example_bool2 = False  # Replace the value True with the value False

print(example_bool1)
print(example_bool2)

